package day22_Nestedloop;

public class Nested_loop {

    public static void main(String[] args) {
        for(int j = 1; j <=10; j++){
            for(int i = 1; i <= 5; i++){
                System.out.print(i +" ");
            }
            System.out.println();
        }

        System.out.println("======================================================");



    }
}
