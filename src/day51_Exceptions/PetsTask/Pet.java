package day51_Exceptions.PetsTask;

public class Pet {

    public String name;
    public int age;
    public char gender;
    public String breed;
    public String color;

    public Pet(String name, int age, char gender, String breed, String color) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.breed = breed;
        this.color = color;
    }



}
